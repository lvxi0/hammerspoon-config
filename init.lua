-- 定义切换应用程序函数
function toggleApplication(name)
    return function()
        local app = hs.application.get(name)
        if not app or not app:mainWindow() then
            hs.application.launchOrFocus(name)
        elseif hs.window.visibleWindows() then
            hs.window.filter.new{name}:getWindows(hs.window.filter.sortByFocusedLast)[1]:focus()
        end
    end
end

-- 设置快捷键来切换应用程序
hs.hotkey.bind({"cmd", "alt", "ctrl"}, "1", toggleApplication("Google Chrome"))
